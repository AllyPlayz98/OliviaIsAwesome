
public class EvenOrOdd {
public static void main(String[] args) {
	for (int a = 0; a<20; a++){
		if (a%2 == 0){
			System.out.println(a+" EVEN");
		}
		else {
			System.out.println(a+" ODD");
		}
	}
	for (int b = 0; b<=30;b++){
		if (b%5 == 0){
			System.out.println(b+" YES");
		}
		else {
			System.out.println(b+ "NO");
		}
	}
}
}
